import CoolProp.CoolProp as CP

# Оставляем только значимые для фазового равновесия компоненты
# Исходные доли: этан (0.004), пропан (0.991995), изобутан (0.003792), н-бутан (0.000208)
# Сумма этих долей = 0.999995. 
# Нормируем до 1.0 (добавим недостающие 0.000005 к основному компоненту - пропану)

fluid = 'HEOS::Ethane[0.004]&Propane[0.992]&IsoButane[0.003792]&n-Butane[0.000208]'
pressure = 1600000  # 1600 кПа

try:
    T_dew_K = CP.PropsSI('T', 'P', pressure, 'Q', 1.0, fluid)
    T_dew_C = T_dew_K - 273.15
    print(f"Температура точки росы: {T_dew_C:.2f} °C")
except ValueError as e:
    print(f"Ошибка: {e}")